def _save(fig, name):
    path = os.path.join(FIG_DIR, name)
    fig.savefig(path, bbox_inches="tight", dpi=150, facecolor=BG_CLR)
    plt.close(fig)
    print(f"  ✔ Saved → {os.path.relpath(path, BASE_DIR)}")

def _section(title, lines, fh):
    fh.write("\n" + "=" * 72 + "\n")
    fh.write(f"  {title}\n")
    fh.write("=" * 72 + "\n")
    for line in lines:
        fh.write(line + "\n")

