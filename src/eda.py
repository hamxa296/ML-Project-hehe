"""
================================================================================
  IEEE-CIS Fraud Detection — Exploratory Data Analysis (EDA)
================================================================================

Purpose
-------
This module performs a thorough, multi-stage EDA on the preprocessed dataset
generated by src/preprocessing.py  →  data/processed_train.csv

It produces:
  • A rich set of publication-quality plots saved to  reports/figures/
  • A plain-text summary report saved to            reports/eda_summary.txt

Why a dedicated EDA module?
---------------------------
EDA is not just "look at some graphs". Done rigorously it:
  1. Validates that our preprocessing worked correctly.
  2. Reveals class imbalance, outliers, and correlation structures that
     directly inform our modelling choices (threshold tuning, class weights,
     feature selection, etc.).
  3. Surfaces temporal / demographic fraud patterns usable as extra features.

Run
---
    python3 src/eda.py          (from project root)
"""

